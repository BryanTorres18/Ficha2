# Modulo Comparaciones
def comparar(num1, num2):
    if num1 == num2:
        print('Ambos valores son iguales')
    else:
        print('Son diferentes')
    if num1 > num2:
        print(f'El mayor es {num1}')
    else:
        print(f'El mayor es {num2}')
    if num1 > num2:
        print(f'El menor es {num2}')
    else:
        print(f'El menor es {num1}')
